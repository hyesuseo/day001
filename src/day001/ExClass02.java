package day001;

public class ExClass02 {

	public static void main(String[] args) {
		System.out.println("안\n녕\n하\n세\n요");
		System.out.println("1\t123456\t6");
		// 1. \t는 줄맞춤을 하고 싶을때 아래와 같이 쓸 수 있다.
		System.out.println("이름\t나이");
		System.out.println("홍길동\t20살");
		
		// 2. "큰따옴표 자체를 출력하고 싶으면 \"로 입력해줘야한다
		System.out.println(" \" ");
		System.out.println(" \" \\ ");
		
		// 3. \자체를 출력하고 싶으면 역슬래쉬를 두 개 넣어주어야 한다.
		System.out.println("\"D:\\평일 핀테크 HS(0923-)\\2. Java\\새            폴더\"");
		
		System.out.println(100 + 100);
		System.out.println(100.1111 + 100.222);
		
		System.out.println("안녕"+ "하세요");
		System.out.println("백" + 100);
		System.out.println("100 "+200);
		
		
		System.out.println("안녕" +100 +200);
		System.out.println("안녕" + (100+200));
		System.out.println();
		
		
		
		

	}

}
